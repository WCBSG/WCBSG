import tkinter as tk
from tkinter import ttk
import random
import re
class QsApp:
    def __init__(s, root):
        s.jd = s.p(判断题,'j')
        s.dx = s.p(单选题,'s')  
        s.ddx = s.p(多选题,'m')
        s.root = root
        root.title("创业知识随机出题")
        root.geometry("800x600")
        s.cw()

    def p(s, t, tp):
        q = []
        if tp == 'j':
            for i in re.findall(r'(\d+)\.(.*?)\n答案：(正确|错误)', t, re.DOTALL):
                q.append({'i':i[0],'q':i[1].strip(),'a':i[2]})
        else:
            for i in re.findall(r'(\d+)\.(.*?)\n((?:[A-D]\..*?\n)+)答案：([A-D,]+)', t, re.DOTALL):
                options = {}
                option_lines = i[2].strip().split('\n')
                for line in option_lines:
                    if line.strip() and len(line) > 2 and line[1] == '.':
                        key = line[0]
                        value = line[2:].strip()
                        options[key] = value
                
                a = i[3].replace(' ','').replace(',','') if tp=='m' else i[3]
                q.append({'i':i[0],'q':i[1].strip(),'o':options,'a':a})
        return q

    def cw(s):
        ttk.Label(s.root,text="创业知识随机出题",font=("SimHei",16,"bold"),padding=10).pack(fill=tk.X)
        f1 = ttk.Frame(s.root,padding=10)
        f1.pack(fill=tk.X)
        ttk.Label(f1,text="题目类型：",font=("SimHei",12)).pack(side=tk.LEFT)
        s.tv = tk.StringVar(value="all")
        for x,v in [("全部","all"),("判断题","j"),("单选题","s"),("多选题","m")]:
            ttk.Radiobutton(f1,text=x,variable=s.tv,value=v).pack(side=tk.LEFT,padx=5)
        ttk.Button(s.root,text="随机出题",command=s.gq,padding=10).pack(fill=tk.X)
        s.qf = ttk.LabelFrame(s.root,text="题目",padding=20)
        s.qf.pack(fill=tk.BOTH,expand=True,padx=10,pady=10)
        s.qt = tk.Text(s.qf,wrap=tk.WORD,font=("SimHei",12),height=8,state=tk.DISABLED)
        s.qt.pack(fill=tk.BOTH,expand=True,pady=10)
        s.of = ttk.Frame(s.qf)
        s.of.pack(fill=tk.BOTH,expand=True,pady=10)
        s.ab = ttk.Button(s.root,text="显示答案",command=s.sa,state=tk.DISABLED,padding=10)
        s.ab.pack(fill=tk.X)
        s.af = ttk.LabelFrame(s.root,text="答案",padding=10)
        s.af.pack(fill=tk.X,padx=10,pady=10)
        s.at = tk.Text(s.af,wrap=tk.WORD,font=("SimHei",12),height=2,state=tk.DISABLED)
        s.at.pack(fill=tk.X,expand=True)

    def gq(s):
        for w in s.of.winfo_children(): w.destroy()
        s.at.config(state=tk.NORMAL)
        s.at.delete(1.0,tk.END)
        s.at.config(state=tk.DISABLED)
        t = s.tv.get()
        if t == "all": p = s.jd + s.dx + s.ddx
        else:
            type_mapping = {'j': 'jd', 's': 'dx', 'm': 'ddx'}
            p = getattr(s, type_mapping[t])
        s.cq = random.choice(p)
        s.ct = "j" if "a" in s.cq and "o" not in s.cq else "s" if len(s.cq.get("a",""))==1 else "m"
        s.qt.config(state=tk.NORMAL)
        s.qt.delete(1.0,tk.END)
        s.qt.insert(tk.END,f"{s.cq['i']}. {s.cq['q']}")
        s.qt.config(state=tk.DISABLED)
        s.v = tk.StringVar() if s.ct != "m" else []
        if s.ct == "j":
            for x in [("正确","正确"),("错误","错误")]:
                ttk.Radiobutton(s.of,text=x[0],variable=s.v,value=x[1]).pack(anchor=tk.W)
        elif s.ct == "s":
            for k,v in s.cq["o"].items():
                ttk.Radiobutton(s.of,text=f"{k}. {v}",variable=s.v,value=k).pack(anchor=tk.W)
        else:
            for k,v in s.cq["o"].items():
                va = tk.IntVar()
                s.v.append((k,va))
                ttk.Checkbutton(s.of,text=f"{k}. {v}",variable=va).pack(anchor=tk.W)
        s.ab.config(state=tk.NORMAL)

    def sa(s):
        s.at.config(state=tk.NORMAL)
        s.at.delete(1.0,tk.END)
        if s.ct == "j":
            r = "✓" if s.v.get() == s.cq["a"] else f"✗ 正确答案:{s.cq['a']}"
        elif s.ct == "s":
            r = "✓" if s.v.get() == s.cq["a"] else f"✗ 正确答案:{s.cq['a']}"
        else:
            ca = sorted(s.cq["a"].replace(' ', '').replace(',', ''))
            ua = sorted([k for k,va in s.v if va.get()==1])
            r = "✓" if ua == ca else f"✗ 正确答案:{', '.join(ca)}"
        s.at.insert(tk.END,r)
        s.at.config(state=tk.DISABLED)

