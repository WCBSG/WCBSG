"""
1.编写一个 Python 程序，实现以下生活场景的功能：
（1）从键盘接收用户输入的一个整数（代表今天的气温，单位：摄氏度）；
（2）使用 if 语句根据气温给出穿衣建议，判断规则如下：
	气温 ≥ 25℃：输出 “今天天气热，建议穿短袖～”；
	气温 <25℃：输出 “今天天气不热，建议穿长袖～”；
（3）要求代码有清晰的输入提示，能正确接收整数输入并输出对应建议。
"""

user = input("请输入今天的气温，单位：摄氏度:")
if int(user>=25):
    print("今天天气热，建议穿短袖～")
else :
    print("“今天天气不热，建议穿长袖～”")
