"""
1.编写一个 Python 程序，判断用户输入的年份是否为闰年，规则如下：
（1）从控制台接收一个整数年份；
（2）闰年判断规则（需同时满足逻辑）：能被 4 整除但不能被 100 整除；或者能被 400 整除；
（3）使用 if 语句实现判断，输出 “XXXX 年是闰年” 或 “XXXX 年不是闰年”；
（4）无需处理非整数输入，仅需按规则判断。
"""

user = input("请输入年份：")
if int(user) %4== 0 and int(user)%100 != 0 or int(user)%400==0:
    print(f"{user}年是闰年")
else:
    print(f"{user}年不是闰年")
